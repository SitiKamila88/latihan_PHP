    <h1>Studi Kasus Penggunaan Perulangan & Kontrol di PHP</h1>
    <p>Berikut adalah berbagai contoh kasus nyata di mana logika perulangan dan kontrol sangat dibutuhkan. 
    Setiap poin juga dijelaskan singkat kenapa perulangan/kontrol relevan di situ.</p>

    <ol>
        <li><b>Menampilkan daftar produk di toko online</b> – Karena produk bisa banyak, looping dipakai untuk menampilkan semuanya otomatis.</li>
        <li><b>Menampilkan data mahasiswa</b> – Data biasanya ratusan, jadi butuh perulangan untuk menampilkan tiap baris dari database.</li>
        <li><b>Pagination (halaman data)</b> – Kontrol if/else dipakai buat tentuin kapan tombol “Next” atau “Previous” aktif.</li>
        <li><b>Validasi nilai ujian</b> – Kontrol if untuk tentuin grade (A, B, C) berdasarkan skor.</li>
        <li><b>Membuat menu navigasi</b> – Perulangan dipakai untuk generate list menu yang jumlahnya dinamis.</li>
        <li><b>Perhitungan bunga bank</b> – Looping bisa dipakai menghitung bunga tiap bulan selama beberapa periode.</li>
        <li><b>Kalender</b> – Looping dipakai untuk generate hari (1–30/31) dan kontrol dipakai untuk cek weekend.</li>
        <li><b>Kalkulasi gaji karyawan</b> – Looping untuk setiap karyawan, kontrol untuk cek lembur/bonus.</li>
        <li><b>Absensi online</b> – Looping untuk list karyawan, kontrol untuk cek hadir/tidak.</li>
        <li><b>Game sederhana</b> – Perulangan untuk langkah permainan, kontrol untuk cek menang/kalah.</li>
        <li><b>Simulasi antrian</b> – Looping untuk proses tiap orang dalam antrian, kontrol untuk cek status selesai.</li>
        <li><b>Form input dinamis</b> – Looping dipakai untuk generate input sesuai kebutuhan, kontrol cek validasi isian.</li>
        <li><b>Chat/messaging app</b> – Looping menampilkan pesan satu per satu, kontrol cek apakah pesan masuk/keluar.</li>
        <li><b>Statistik website</b> – Looping menghitung jumlah visitor harian, kontrol cek batas tertentu (misalnya target tercapai).</li>
        <li><b>Pencarian data</b> – Looping untuk scan semua data, kontrol if untuk cocokin keyword.</li>
        <li><b>Upload banyak file</b> – Looping memproses tiap file, kontrol cek tipe file dan ukuran.</li>
        <li><b>Notifikasi</b> – Looping untuk semua user, kontrol tentuin siapa yang berhak dapat notifikasi.</li>
        <li><b>Simulasi perhitungan stok</b> – Looping cek tiap barang, kontrol tentuin stok habis atau tidak.</li>
        <li><b>Quiz online</b> – Looping untuk tampilkan soal satu per satu, kontrol cek jawaban benar/salah.</li>
        <li><b>Pembuatan laporan bulanan</b> – Looping ambil data setiap bulan, kontrol tentuin mana yang ditampilkan (misalnya bulan tertentu).</li>
    </ol>

